    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(tblPO.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati poreskog obveznika koda zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                PoreskiObveznik po = ((TableModelPoreskiObveznik) tblPO.getModel()).getSelectedObject(tblPO.getSelectedRow());
                AddPoreskiObveznikForma form = new AddPoreskiObveznikForma(this, true, po, "UPDATE");
                form.setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(tblPO.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati poreskog obveznika koda zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                PoreskiObveznik po = ((TableModelPoreskiObveznik) tblPO.getModel()).getSelectedObject(tblPO.getSelectedRow());
                Controller.getInstance().delete(po);
                refreshTable();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

