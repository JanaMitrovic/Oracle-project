    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(tblFilijala.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati filijalu!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Filijala f = ((TableModelFilijala) tblFilijala.getModel()).getSelectedObject(tblFilijala.getSelectedRow());
                Mesto mesto = (Mesto) cmbMesto.getSelectedItem();
                f.setMesto(mesto);
                Controller.getInstance().update(f);
                refreshTable();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

