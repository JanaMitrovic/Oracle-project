    private void fillTable() {
        try {
            List<PoreskiObveznik> list = Controller.getInstance().getPoreskiObveznikList();
            TableModelPoreskiObveznik modelPO = new TableModelPoreskiObveznik(list);
            tblPO.setModel(modelPO);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska prilikom ucitavanja!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    
    public void refreshTable(){
        List<PoreskiObveznik> shownPO = loadObjects();
        TableModelPoreskiObveznik modelPO = (TableModelPoreskiObveznik) tblPO.getModel();
        modelPO.setList(shownPO);
        modelPO.fireTableDataChanged();
    }
    
     private List<PoreskiObveznik> loadObjects() {
        try {
            return Controller.getInstance().getPoreskiObveznikList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }
