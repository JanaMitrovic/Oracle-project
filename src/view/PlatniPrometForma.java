    private void fillTable() {
        try {
            List<PlatniPromet> list = Controller.getInstance().getPlatniPrometList();
            TableModelPlatniPromet modelPP = new TableModelPlatniPromet(list);
            tblPP.setModel(modelPP);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska prilikom ucitavanja!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    
    public void refreshTable(){
        this.shownPP = loadObjects();
        TableModelPlatniPromet modelPP = (TableModelPlatniPromet) tblPP.getModel();
        modelPP.setList(shownPP);
        modelPP.fireTableDataChanged();
    }
    
     private List<PlatniPromet> loadObjects() {
        try {
            return Controller.getInstance().getPlatniPrometList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }

