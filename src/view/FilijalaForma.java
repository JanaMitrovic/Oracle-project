    private void fillTable() {
        try {
            List<Filijala> list = Controller.getInstance().getFilijalaList();
            TableModelFilijala modelF = new TableModelFilijala(list);
            tblFilijala.setModel(modelF);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska prilikom ucitavanja!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    private void fillCmbMesto() {
        try {
            cmbMesto.removeAllItems();
            List<Mesto> mesta = Controller.getInstance().getMestaList();
            for (Mesto mesto : mesta) {
                cmbMesto.addItem(mesto);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom ucitavanja mesta!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void refreshTable(){
        List<Filijala> shownF = loadObjects();
        TableModelFilijala modelF = (TableModelFilijala) tblFilijala.getModel();
        modelF.setList(shownF);
        modelF.fireTableDataChanged();
    }
    
     private List<Filijala> loadObjects() {
        try {
            return Controller.getInstance().getFilijalaList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }
