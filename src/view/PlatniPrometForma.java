    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(tblPP.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati platni promet koji zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                PlatniPromet pp = ((TableModelPlatniPromet) tblPP.getModel()).getSelectedObject(tblPP.getSelectedRow());
                AddPlatniPrometForma form = new AddPlatniPrometForma(this, true, pp, "UPDATE");
                form.setVisible(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(tblPP.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(this, "Morate izabrati platni promet koji zelite da izmenite!", "Greska", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                PlatniPromet pp = ((TableModelPlatniPromet) tblPP.getModel()).getSelectedObject(tblPP.getSelectedRow());
                Controller.getInstance().delete(pp);
                refreshTable();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

